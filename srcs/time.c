#include "philo.h"

long	get_time(void)
{
	struct timeval	t;

	gettimeofday(&t, NULL);
	return ((t.tv_sec * 1000) + (t.tv_usec / 1000));
}

long	diff_time(long t1, long t2)
{
	return (t2 - t1);
}
